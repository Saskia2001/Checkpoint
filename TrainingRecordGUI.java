    // listen for and respond to GUI events 
    public void actionPerformed(ActionEvent event) {
        String message = "";
        if (event.getSource() == addR) {
            message = addEntry("generic");
        }
        if (event.getSource() == lookUpByDate) {
            message = lookupEntry();
        }
        if (event.getSource() == findAllByDate) {
        	message = findAllEntries();
        }
        if (event.getSource() == remove) {
        	message = remove();
        }
        outputArea.setText(message);
        blankDisplay();
    } // actionPerformed

    private String remove() {
    	int m = Integer.parseInt(month.getText());
        int d = Integer.parseInt(day.getText());
        int y = Integer.parseInt(year.getText());
        outputArea.setText("looking up record ...");
        String message = myAthletes.removeEntry(d, m, y);
        return message;
	}

	public String addEntry(String what) {
        String message = "Record added\n";
        System.out.println("Adding "+what+" entry to the records");
        String n = name.getText();
        int m = Integer.parseInt(month.getText());
        int d = Integer.parseInt(day.getText());
        int y = Integer.parseInt(year.getText());
        float km = java.lang.Float.parseFloat(dist.getText());
        int h = Integer.parseInt(hours.getText());
        int mm = Integer.parseInt(mins.getText());
        int s = Integer.parseInt(secs.getText());
        String sportType = sport.getText();
        System.out.println(sportType);
        if (sportType.equals("s")) {
        	System.out.println("here");
        	String where = field1.getText();
        	SwimEntry e = new SwimEntry(n, d, m, y, h, mm, s, km, where);
            myAthletes.addEntry(e);
            return message;
        }
        if (sportType.equals("c")) {
        	String terrain = field1.getText();
        	String tempo = field2.getText();
        	CycleEntry e = new CycleEntry(n, d, m, y, h, mm, s, km, terrain, tempo);
            myAthletes.addEntry(e);
            return message;
        }
        if (sportType.equals("r")) {
        	int repetitions = Integer.parseInt(field1.getText());
        	int recovery = Integer.parseInt(field2.getText());
        	SprintEntry e = new SprintEntry(n, d, m, y, h, mm, s, km, repetitions, recovery);
            myAthletes.addEntry(e);
            return message;
        }
        return "error";
    }
    
    public String lookupEntry() {
        int m = Integer.parseInt(month.getText());
        int d = Integer.parseInt(day.getText());
        int y = Integer.parseInt(year.getText());
        outputArea.setText("looking up record ...");
        String message = myAthletes.lookupEntry(d, m, y);
        return message;
    }
    
    public String findAllEntries() {
    	int m = Integer.parseInt(month.getText());
        int d = Integer.parseInt(day.getText());
        int y = Integer.parseInt(year.getText());
        outputArea.setText("looking up record ...");
        String message= myAthletes.findAllEntries(d, m, y);
        return message;
    }

